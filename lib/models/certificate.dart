class Certificate {
  final String id_riwayat;
  final String nama_sertifikasi;
  final String jenis_sertifikasi;
  final String level_sertifikasi;
  final String no_sertifikat;
  final String tanggal_terbit;
  final String masa_berlaku;
  final String penyelenggara;
  final String dokumen_sertifikat;

  Certificate({
    required this.id_riwayat,
    required this.nama_sertifikasi,
    required this.jenis_sertifikasi,
    required this.level_sertifikasi,
    required this.no_sertifikat,
    required this.tanggal_terbit,
    required this.masa_berlaku,
    required this.penyelenggara,
    required this.dokumen_sertifikat,
  });
}
